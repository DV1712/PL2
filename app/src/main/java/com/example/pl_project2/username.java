package com.example.pl_project2;

public class username {
    String username;
    String password;
    public username()
    {

    }
    public username(String username, String password)
    {
        this.username = username;
        this.password = password;
    }

    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return password;
    }
}
